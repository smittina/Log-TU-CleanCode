package ex1;

/**
 * Classe qui réalise des opérations arythmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne le résultat de l'addition entre a et b
	 * 
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return le résultat de l'addition
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode qui retourne le résultat de la soustraction de b à a
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return le résultat de la soustraction
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
